package variablesAndDataTypes

class Example2 {

    companion object {

        fun execute() {
            // Números sem ponto flutuante (Byte, Short, Int, Long)
            var _byte: Byte = 1;
            var _short: Short = 2;
            var _int: Int = 100;
            var _long: Long = 200;
            var _longCompile = 100L;

            // Números com ponto flutuante (Double, Float)
            val _double = 14.5;
            val _float = 20F;
        }
    }

}